def add_one(x):
    """
    Adds one to a number and returns the result. HELLO

    :param kind: Number to incremented
    :type kind: int
    :raise: ValueError: if x is non-numeric
    :return: incremented value
    :rtype: int
    """
    return x + 1


def subtract_one(x):
    """
    Subtracts one to a number and returns the result.

    :param kind: Number to decremented
    :type kind: int
    :raise ValueError: if x is non-numeric
    :return: decremented value
    :rtype: int
    """
    return x - 1
